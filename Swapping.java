public class Swapping{
    public static void main(String args[]){
       // Scanner sc=new Scanner(System.in);
        int a=100;
        int b=20;
        int temp;
        System.out.println("before swapping"+a);
        System.out.println("before swapping"+b);
        temp=a;
        a=b;
        b=temp; 
        System.out.println("after swapping"+a );
        System.out.println("before swapping"+b);
    }
}