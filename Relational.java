public class Relational{
    public static void main(String args[]) 
    {
        float a=1.23f;
        float b=5.6f;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b); 
    }
}